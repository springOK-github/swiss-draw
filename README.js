/**
 * ポケモンカード・ガンスリンガーバトル用マッチングシステム
 * @fileoverview システム概要と構成の説明
 * @author SpringOK
 *
 * @description
 * Google Apps Script (GAS) とスプレッドシートで動作する
 * ポケモンカードバトル大会のマッチングシステム
 *
 * プレイヤーの状態遷移:
 * 1. 待機: マッチング待ち
 * 2. 対戦中: 対戦進行中
 * 3. 休憩: 一時的に対戦から離脱（復帰可能）
 * 4. 終了: ドロップアウト済み（復帰不可）
 *
 * 主な機能:
 * - プレイヤー登録・管理（自動ID採番）
 * - 自動マッチング（再戦回避、勝者優先）
 * - 対戦結果記録（統計自動更新）
 * - プレイヤー休憩・復帰処理
 * - プレイヤードロップアウト処理
 * - 卓番号の自動管理と再利用
 * - 排他制御（複数ユーザー対応）
 *
 * ファイル構成:
 * 
 * ドメイン層:
 * - player-domain.js: プレイヤードメイン（操作、データ取得、統計更新、状態遷移）
 * - match-domain.js: 対戦ドメイン（マッチング管理、結果記録・修正）
 * 
 * 共通層:
 * - shared.js: 共有ユーティリティ（シート操作、UI共通処理）
 * 
 * アプリケーション層:
 * - app.js: アプリケーション層（初期化、設定管理、排他制御）
 * 
 * その他:
 * - constants.js: 定数定義
 * - test-utils.js: テスト用関数
 */