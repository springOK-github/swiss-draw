/**
 * ポケモンカード・ガンスリンガーバトル用マッチングシステム
 * @fileoverview システム概要と構成の説明
 * @author SpringOK
 *
 * @description
 * Google Apps Script (GAS) とスプレッドシートで動作する
 * ポケモンカードバトル大会のマッチングシステム
 *
 * プレイヤーの状態遷移:
 * 1. 待機: マッチング待ち
 * 2. 対戦中: 対戦進行中
 * 3. 休憩: 一時的に対戦から離脱（復帰可能）
 * 4. 終了: ドロップアウト済み（復帰不可）
 *
 * 主な機能:
 * - プレイヤー登録・管理（自動ID採番）
 * - 自動マッチング（再戦回避、勝者優先）
 * - 対戦結果記録（統計自動更新）
 * - プレイヤー休憩・復帰処理
 * - プレイヤードロップアウト処理
 * - 卓番号の自動管理と再利用
 * - 排他制御（複数ユーザー対応）
 *
 * ファイル構成:
 * - constants.js: 定数定義
 * - setup.js: 初期設定とメニュー関連
 * - match-manager.js: マッチング、結果記録等のメインロジック
 * - player-manager.js: プレイヤーのライフサイクル管理
 * - player-state.js: 状態遷移の共通処理
 * - sheet-utils.js: シート操作ヘルパー
 * - lock-utils.js: 排他制御
 * - test-utils.js: テスト用関数
 */