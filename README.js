/**
 * スイス方式トーナメントマッチングシステム
 * @fileoverview システム概要と構成の説明
 * @author springOK
 *
 * @description
 * Google Apps Script (GAS) とスプレッドシートで動作する
 * スイス方式トーナメントのマッチングシステム
 *
 * プレイヤーの状態:
 * 1. 参加中: トーナメント進行中
 * 2. 終了: ドロップアウト済み（以降のラウンドに参加しない）
 *
 * 主な機能:
 * - プレイヤー登録・管理（自動ID採番）
 * - ラウンド管理（ラウンド開始）
 * - スイス方式マッチング（同勝点ペアリング、再戦回避、Bye処理）
 * - 対戦結果記録（勝敗・引き分け、統計自動更新）
 * - 勝率計算（タイブレーク用）
 * - 順位表示（勝点、勝率、自己勝率による順位付け）
 * - プレイヤードロップアウト処理
 * - 卓番号の自動管理
 * - 排他制御（複数ユーザー対応）
 *
 * ファイル構成:
 *
 * ドメイン層:
 * - player-domain.js: プレイヤードメイン（登録、統計、順位表示）
 * - match-domain.js: 対戦ドメイン（スイス方式マッチング、結果記録・修正）
 * - round-manager.js: ラウンド管理（ライフサイクル管理）
 *
 * 共通層:
 * - shared.js: 共有ユーティリティ（シート操作、UI共通処理）
 *
 * アプリケーション層:
 * - app.js: アプリケーション層（初期化、設定管理、排他制御）
 *
 * その他:
 * - constants.js: 定数定義
 * - test-utils.js: テスト用関数
 */
